<?php 
// Controle de fluxo (loops) - Laço While
# São utilizadas para controlar um código ou bloco de código repetidas vezes.

# Para este controle de fluxo (while) o código pode nunca ser executado, pois o teste lógico pode inibir a execução do código.

# Exemplo 
$x = 1;
while($x<10){
    echo "Exemplo 1 ";
    echo "ciclo while em execução " . $x;
    echo "<br>";
    $x++;
}

$x = 1;
while($x<10){
    echo "Exemplo 2 ";
    echo "ciclo while em execução " . $x;
    echo "<br>";
    $x++;
 }

?>